from typing import List


class Solution:
    def largestRectangleArea(self, heights: List[int]) -> int:
        """
        å•è°ƒæ ˆ+å“¨å…µ
        :param heights:
        :return:
        """
        size = len(heights)
        heights = [0]+ heights + [0]
        size +=1
        res = 0
        stack = [0]
        for i in range(size):
            while heights[stack[-1]]>heights[i]:
                curh = heights[stack.pop()]
                curw = i-stack[-1]-1
                res = max(res,curh*curw)
            stack.append(i)
        return res
